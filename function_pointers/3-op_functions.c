#include "3-calc.h"

/**
 *op_add- gets users input
 *@a: stores first variable
 *@b: stores second variable
 *Return: results
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 *op_sub- gets users input
 *@a: stores first variable
 *@b: stores second variable
 *Return: results
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 *op_mul- gets users input
 *@a: stores first variable
 *@b: stores second variable
 *Return: results
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 *op_div- gets users input
 *@a: stores first variable
 *@b: stores second variable
 *Return: results
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 *op_mod- gets users input
 *@a: stores first variable
 *@b: stores second variable
 *Return: results
 */

int op_mod(int a, int b)
{
return (a % b);
}

